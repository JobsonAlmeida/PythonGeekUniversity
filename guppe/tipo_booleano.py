"""
Tipo Booleano

Álgebra booleana criada por George Boole

True -> verdadeiro
False -> falso

obs.: sempre com a inicial maiúscula
"""

ativo = False

print(ativo)

"""
Operações básicas
"""

# Negação (not)

estado = not ativo

print(estado)

# Operação or
# It's a binary operation

logado = False

print("ativo or logado")
print(ativo or logado)

# Operação and

print (ativo and logado)

# Comparação


